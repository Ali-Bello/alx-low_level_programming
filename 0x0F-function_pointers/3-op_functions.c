#include "3-calc.h"

/**
  * op_add - adds a & b.
  * @a: number1.
  * @b: number2.
  * Return: the sum.
  */

int	op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - a minus b.
  * @a: num1.
  * @b: num2.
  * Return: the difference .
  */

int	op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplies a by b.
  * @a: num1.
  * @b: num2.
  * Return: the multiplication.
  */

int	op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - devides a by b.
  * @a: num1.
  * @b: num2.
  * Return: the devision.
  */

int	op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - the rest of devision of a by b.
  * @a: num1.
  * @b: num2.
  * Return: the modulo.
  */

int	op_mod(int a, int b)
{
	return (a % b);
}
